/**
 * PostgREST フィルタ向けのユーザー入力サニタイズ。
 *
 * supabase-js の `.or()` / `.ilike()` はフィルタ文字列を素のまま組み立てるため、
 * ユーザー入力にカンマ・括弧・ピリオドなどが含まれると、意図しない条件が
 * 注入されうる（フィルタインジェクション）。また ilike のパターン中の
 * `%` `_` `\\` はワイルドカード/エスケープ文字として解釈される。
 *
 * ここでは ilike の検索語として安全に使えるよう、
 * - PostgREST の構文区切り文字（, ( ) . : *）を空白へ置換
 * - LIKE のワイルドカード（% _）と LIKE のエスケープ文字（\\）を除去
 * - 制御文字（U+0000〜U+001F, U+007F）を除去
 * してから返す。正規の検索語（日本語・英数字・記号の一部）はそのまま通る。
 */
export function sanitizeLikePattern(input: string): string {
  return (
    input
      // PostgREST のフィルタ構文に使われる区切り文字を無害化
      .replace(/[,().:*]/g, ' ')
      // LIKE のワイルドカード/エスケープ文字を除去
      .replace(/[%_\\]/g, '')
      // 制御文字（改行・タブ等）を除去
      .replace(/[\x00-\x1f\x7f]/g, '')
      .trim()
  )
}
